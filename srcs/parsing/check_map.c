#include "../../includes/cub3d.h"

int	check_extension(char *str)
{
	if (ft_strlen(str) <= 4)
		return (1);
	if (ft_strncmp(str + (ft_strlen(str) - 4), ".cub", 4) == 0)
		return (0);
	return (1);
}
